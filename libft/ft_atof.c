#include "libft.h"

float	ft_atof(char **s, int i, float x, float signe)
{
	while ((**s >= 8 && **s <= 13) || **s == 32 || **s == ',')
		(*s)++;
	if (**s == '-')
	{
		signe = -1;
		(*s)++;
	}
	else if (**s == '+')
		(*s)++;
	while (**s >= '0' && **s <= '9')
	{
		x = x * 10 + (**s - '0');
		(*s)++;
	}
	if (**s == '.')
	{
		(*s)++;
		while (**s >= '0' && **s <= '9')
		{
			x += (**s - '0') * pow(10, i--);
			(*s)++;
		}
	}
	return (signe * x);
}
