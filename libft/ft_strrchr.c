#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	str;

	str = (char)c;
	i = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == str)
			return (char*)(s + i);
		i--;
	}
	return (NULL);
}
